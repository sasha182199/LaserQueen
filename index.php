		<p class="text">
			 Лазерная эпиляция — это метод радикального удаления волос, с разрушением волосяных фолликулов при помощи лазерного излучения (свет с одной длиной волны с высокой направленностью и большой плотностью энергии). <br><br>

			Лазерная эпиляция является косметологической медицинской процедурой и осуществляется с использованием специального прибора — лазера, как правило диодного или александритового. Пигмент волоса — меланин, который сосредоточен в стержне и волосяном фолликуле, способен поглощать световые волны определённой длины (оптимальный диапазон 700-800 нм). В результате действия лазерного излучения меланин, поглощая энергию света, нагревается и разрушает соседние клетки матрикса (ростковая зона волоса), кровеносные и лимфатические сосуды, питающие волосяной фолликул, и, по-видимому, сальные железы волос. Спустя несколько недель стержень волоса с погибшим «корнем» выпадает. <br><br>

			Часть волосяных фолликулов находится в спящем состоянии (около 80-90%) и 10-20% находится в активном росте. Для максимального удаления волос (90-100%), необходима серия процедур эпиляции с интервалом 3-6 недель, в зависимости от обрабатываемой лазером зоны и индивидуальных особенностей организма, напрямую влияющих на темпы роста волос. <br><br>

			Количество процедур как правило составляет 6-10 и зависит как от мощности лазерной системы, так и от индивидуальной предрасположенности к появлению новых волос. В период гормональных изменений курс лазерной эпиляции может не дать эффекта полного избавления от появления новых волос. <br><br>
